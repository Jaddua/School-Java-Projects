//import required libraries
import java.lang.Math;
import java.util.Scanner;
//Door class
public class Door {
	//declaring variables and data types
	private double height;
	private double width;
//constructor
public Door() {
}
//getter method for the height of the door
public double getHeight() {
	return this.height;
}
//getter method for the width of the door
public double getWidth() {
	return this.width;
}
//setter method for the height of the door
public void setHeight(double height) {
	this.height = height;
}
//setter method for the width of the door
public void setWidth(double width) {
	this.width = width;
}


}
